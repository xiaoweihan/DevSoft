//文件位置定义
enum FILEPOS
{
	//当前位置
	POS_CURRENT=0,
	//文件头
	POS_HEAD,
	//文件尾
	POS_TAIL
};

//日志等级定义
enum LOGLEVEL
{
	LOG_DEBUG = 0,
	LOG_INFO,
	LOG_WARN,
	LOG_ERROR,
	LOG_FATAL,
	LOG_NECESSARY
};
